#!/usr/bin/env node
/**
 * PrimeTeam — Claude Task Heartbeat Hook (v2 with granular events)
 *
 * Posts a heartbeat AND a granular event to `claude-activity-ingest` on each
 * PostToolUse so the "Tempo Reale" tab can show both a session aggregate and
 * a chronological timeline of what's actually happening.
 *
 * Privacy contract — UNCHANGED:
 *  - Only sends paths, git branch, last 10 commit messages, and a short
 *    privacy-safe "action summary" (e.g. "Edit Tarefas.tsx", "Bash git push").
 *  - NEVER sends file contents, tool outputs, prompts, or full Bash commands.
 *  - Hard 1.5s timeout; exits 0 always (never blocks Claude Code).
 *
 * Install (manual wire-up in ~/.claude/settings.json):
 *   {
 *     "hooks": {
 *       "PostToolUse": [
 *         {
 *           "matcher": "Edit|Write|Bash|MultiEdit|Read",
 *           "hooks": [{ "type": "command",
 *                       "command": "node ~/.claude/hooks/claude-task-heartbeat.cjs" }]
 *         }
 *       ]
 *     }
 *   }
 *
 * Required:
 *   ~/.primeteam/session.json  — generated by `pto login`. Holds access_token.
 */

'use strict';

const fs = require('node:fs');
const path = require('node:path');
const os = require('node:os');
const https = require('node:https');
const { execSync } = require('node:child_process');
const crypto = require('node:crypto');

process.on('uncaughtException', () => process.exit(0));
process.on('unhandledRejection', () => process.exit(0));

const SUPABASE_URL = 'https://xmqmuxwlecjbpubjdkoj.supabase.co';
const SUPABASE_ANON_KEY = 'eyJhbGciOiJIUzI1NiIsInR5cCI6IkpXVCJ9.eyJpc3MiOiJzdXBhYmFzZSIsInJlZiI6InhtcW11eHdsZWNqYnB1Ympka29qIiwicm9sZSI6ImFub24iLCJpYXQiOjE3NzIzNzQzNzAsImV4cCI6MjA4Nzk1MDM3MH0.fzHfYzJNeCUnG6DjoHYPPbUg3Q1paMPGaDruiDGe1MU';
const SESSION_PATH = path.join(os.homedir(), '.primeteam', 'session.json');
const SESSION_ID_PATH = path.join(os.homedir(), '.primeteam', `claude-session-${process.ppid}.txt`);
const REQUEST_TIMEOUT_MS = 1500;

function readJSON(p) {
  try { return JSON.parse(fs.readFileSync(p, 'utf8')); } catch { return null; }
}

function loadAccessToken() {
  const s = readJSON(SESSION_PATH);
  if (!s?.access_token) return null;
  if (s.expires_at && Number(s.expires_at) < Math.floor(Date.now() / 1000)) {
    return null;
  }
  return s.access_token;
}

function getOrCreateSessionId() {
  try {
    if (fs.existsSync(SESSION_ID_PATH)) {
      return fs.readFileSync(SESSION_ID_PATH, 'utf8').trim();
    }
  } catch {}
  const id = crypto.randomUUID();
  try {
    fs.mkdirSync(path.dirname(SESSION_ID_PATH), { recursive: true });
    fs.writeFileSync(SESSION_ID_PATH, id);
  } catch {}
  return id;
}

function readStdinSync() {
  try {
    const buf = fs.readFileSync(0);
    return JSON.parse(buf.toString('utf8'));
  } catch {
    return null;
  }
}

function safeExec(cmd, opts = {}) {
  try { return execSync(cmd, { encoding: 'utf8', stdio: ['ignore', 'pipe', 'ignore'], timeout: 800, ...opts }).trim(); }
  catch { return ''; }
}

/**
 * Build a short, privacy-safe summary of THIS specific tool call.
 *  - Edit/Write/MultiEdit/Read → "Edit Tarefas.tsx", "Read useTaskDashboardAnalytics.ts"
 *  - Bash → "Bash: git push", "Bash: npm run lint" — first 2 tokens of cmd.
 *  - Other → tool_name alone.
 * NEVER includes full file contents, Bash flags after the command name, etc.
 */
function buildEventSummary(event) {
  const tool = event?.tool_name ?? null;
  const input = event?.tool_input ?? {};
  if (!tool) return null;

  if (tool === 'Bash') {
    const cmd = typeof input.command === 'string' ? input.command : '';
    // Take only the first segment (binary + 1 subcommand). E.g. "git push ..." → "git push"
    const tokens = cmd.trim().split(/\s+/).slice(0, 2).join(' ');
    return tokens ? `Bash: ${tokens}` : 'Bash';
  }

  // Path-based tools
  const filePath = typeof input.file_path === 'string'
    ? input.file_path
    : (typeof input.path === 'string' ? input.path : null);
  if (filePath) {
    const base = path.basename(filePath);
    return `${tool} ${base}`;
  }

  if (Array.isArray(input.edits) && input.edits.length > 0) {
    const fp = input.edits[0]?.file_path;
    if (typeof fp === 'string') return `${tool} ${path.basename(fp)} (+${input.edits.length - 1})`;
  }

  return tool;
}

function gatherContext(event) {
  const paths = new Set();
  const toolInput = event?.tool_input ?? {};
  ['file_path', 'path', 'notebook_path'].forEach((k) => {
    if (typeof toolInput[k] === 'string') paths.add(toolInput[k]);
  });
  if (Array.isArray(toolInput.edits)) {
    toolInput.edits.forEach((e) => { if (typeof e?.file_path === 'string') paths.add(e.file_path); });
  }

  const branch = safeExec('git rev-parse --abbrev-ref HEAD');
  const recentMsgs = safeExec('git log -10 --pretty=format:%s');
  const commitMsgs = recentMsgs ? recentMsgs.split('\n').slice(0, 10) : [];

  // Always capture cwd basename — works even when tool_input has no path
  // (e.g. Bash). Falls back to git toplevel if available.
  const gitRoot = safeExec('git rev-parse --show-toplevel');
  const repoRoot = gitRoot || process.cwd();
  const repo = repoRoot ? path.basename(repoRoot) : null;

  // Event details (one row per tool use in the timeline)
  const eventPath = paths.size > 0 ? Array.from(paths)[0] : null;
  const eventSummary = buildEventSummary(event);

  return {
    paths: Array.from(paths),
    branch: branch || null,
    commitMsgs,
    tool: event?.tool_name ?? null,
    repo: repo || null,
    cwd: process.cwd(),
    event: {
      tool_name: event?.tool_name ?? null,
      path: eventPath,
      summary: eventSummary,
    },
  };
}

function post(token, body) {
  return new Promise((resolve) => {
    const data = Buffer.from(JSON.stringify(body), 'utf8');
    const req = https.request(
      `${SUPABASE_URL}/functions/v1/claude-activity-ingest`,
      {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
          'apikey': SUPABASE_ANON_KEY,
          'Authorization': `Bearer ${token}`,
          'Content-Length': data.length,
        },
        timeout: REQUEST_TIMEOUT_MS,
      },
      (res) => {
        res.on('data', () => {});
        res.on('end', () => resolve());
      },
    );
    req.on('error', () => resolve());
    req.on('timeout', () => { req.destroy(); resolve(); });
    req.write(data);
    req.end();
  });
}

(async () => {
  const token = loadAccessToken();
  if (!token) process.exit(0);

  const event = readStdinSync();
  if (!event) process.exit(0);

  const sessionId = getOrCreateSessionId();
  const ctx = gatherContext(event);
  // Always send if we have a tool name OR repo OR branch (Bash without paths still useful)
  if (!ctx.tool && !ctx.repo && !ctx.branch) process.exit(0);

  await post(token, {
    session_id: sessionId,
    paths: ctx.paths,
    branch: ctx.branch,
    commit_msgs: ctx.commitMsgs,
    tool_name: ctx.tool,
    repo: ctx.repo,
    cwd: ctx.cwd,
    event: ctx.event,
  });

  process.exit(0);
})();
