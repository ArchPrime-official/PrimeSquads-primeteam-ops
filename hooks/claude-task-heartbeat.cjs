#!/usr/bin/env node
/**
 * PrimeTeam — Claude Task Heartbeat Hook
 *
 * Posts a heartbeat to `claude-activity-ingest` on each PostToolUse event so
 * the "Attività in tempo reale" tab in /tarefas knows what you're working on
 * and can auto-fill `started_at` on the matching task.
 *
 * Privacy:
 *  - Only sends paths, git branch, and the last 10 commit messages.
 *  - NEVER sends file contents, tool outputs, or user prompts.
 *  - Hard 1.5s timeout; exits 0 always (never blocks Claude Code).
 *
 * Install:
 *   pto setup    # interactive — will copy this file to ~/.claude/hooks/
 *                # and wire it up in ~/.claude/settings.json
 *
 * Manual wire-up (~/.claude/settings.json):
 *   {
 *     "hooks": {
 *       "PostToolUse": [
 *         {
 *           "matcher": "Edit|Write|Bash|MultiEdit",
 *           "hooks": [
 *             { "type": "command",
 *               "command": "node ~/.claude/hooks/claude-task-heartbeat.cjs" }
 *           ]
 *         }
 *       ]
 *     }
 *   }
 *
 * Required env vars / files:
 *   ~/.primeteam/session.json  — generated by `pto login`. Holds access_token.
 *
 * Session ID:
 *   Persists in ~/.primeteam/claude-session-{pid}.txt so successive
 *   PostToolUse events in the same Claude Code session share one heartbeat row.
 */

'use strict';

const fs = require('node:fs');
const path = require('node:path');
const os = require('node:os');
const https = require('node:https');
const { execSync } = require('node:child_process');
const crypto = require('node:crypto');

process.on('uncaughtException', () => process.exit(0));
process.on('unhandledRejection', () => process.exit(0));

const SUPABASE_URL = 'https://xmqmuxwlecjbpubjdkoj.supabase.co';
const SUPABASE_ANON_KEY = 'eyJhbGciOiJIUzI1NiIsInR5cCI6IkpXVCJ9.eyJpc3MiOiJzdXBhYmFzZSIsInJlZiI6InhtcW11eHdsZWNqYnB1Ympka29qIiwicm9sZSI6ImFub24iLCJpYXQiOjE3NzIzNzQzNzAsImV4cCI6MjA4Nzk1MDM3MH0.fzHfYzJNeCUnG6DjoHYPPbUg3Q1paMPGaDruiDGe1MU';
const SESSION_PATH = path.join(os.homedir(), '.primeteam', 'session.json');
const SESSION_ID_PATH = path.join(os.homedir(), '.primeteam', `claude-session-${process.ppid}.txt`);
const REQUEST_TIMEOUT_MS = 1500;

function readJSON(p) {
  try { return JSON.parse(fs.readFileSync(p, 'utf8')); } catch { return null; }
}

function loadAccessToken() {
  const s = readJSON(SESSION_PATH);
  if (!s?.access_token) return null;
  // Don't even attempt if obviously expired (defensive)
  if (s.expires_at && Number(s.expires_at) < Math.floor(Date.now() / 1000)) {
    return null; // user needs to `pto refresh`; silent skip
  }
  return s.access_token;
}

function getOrCreateSessionId() {
  try {
    if (fs.existsSync(SESSION_ID_PATH)) {
      return fs.readFileSync(SESSION_ID_PATH, 'utf8').trim();
    }
  } catch {}
  const id = crypto.randomUUID();
  try {
    fs.mkdirSync(path.dirname(SESSION_ID_PATH), { recursive: true });
    fs.writeFileSync(SESSION_ID_PATH, id);
  } catch {}
  return id;
}

function readStdinSync() {
  // Claude Code passes event JSON on stdin
  try {
    const buf = fs.readFileSync(0);
    return JSON.parse(buf.toString('utf8'));
  } catch {
    return null;
  }
}

function safeExec(cmd, opts = {}) {
  try { return execSync(cmd, { encoding: 'utf8', stdio: ['ignore', 'pipe', 'ignore'], timeout: 800, ...opts }).trim(); }
  catch { return ''; }
}

function gatherContext(event) {
  const paths = new Set();
  const toolInput = event?.tool_input ?? {};
  // Common path-bearing keys across Edit/Write/MultiEdit/Read tools
  ['file_path', 'path', 'notebook_path'].forEach((k) => {
    if (typeof toolInput[k] === 'string') paths.add(toolInput[k]);
  });
  if (Array.isArray(toolInput.edits)) {
    toolInput.edits.forEach((e) => { if (typeof e?.file_path === 'string') paths.add(e.file_path); });
  }

  const branch = safeExec('git rev-parse --abbrev-ref HEAD');
  const recentMsgs = safeExec('git log -10 --pretty=format:%s');
  const commitMsgs = recentMsgs ? recentMsgs.split('\n').slice(0, 10) : [];

  return {
    paths: Array.from(paths),
    branch: branch || null,
    commitMsgs,
    tool: event?.tool_name ?? null,
  };
}

function post(token, body) {
  return new Promise((resolve) => {
    const data = Buffer.from(JSON.stringify(body), 'utf8');
    const req = https.request(
      `${SUPABASE_URL}/functions/v1/claude-activity-ingest`,
      {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
          'apikey': SUPABASE_ANON_KEY,
          'Authorization': `Bearer ${token}`,
          'Content-Length': data.length,
        },
        timeout: REQUEST_TIMEOUT_MS,
      },
      (res) => {
        // Drain body and resolve; we don't care about the response shape
        res.on('data', () => {});
        res.on('end', () => resolve());
      },
    );
    req.on('error', () => resolve());
    req.on('timeout', () => { req.destroy(); resolve(); });
    req.write(data);
    req.end();
  });
}

(async () => {
  const token = loadAccessToken();
  if (!token) process.exit(0); // not logged in or expired — silent skip

  const event = readStdinSync();
  if (!event) process.exit(0);

  const sessionId = getOrCreateSessionId();
  const ctx = gatherContext(event);
  if (ctx.paths.length === 0 && !ctx.branch) process.exit(0); // nothing useful to ping

  await post(token, {
    session_id: sessionId,
    paths: ctx.paths,
    branch: ctx.branch,
    commit_msgs: ctx.commitMsgs,
    tool_name: ctx.tool,
  });

  process.exit(0);
})();
